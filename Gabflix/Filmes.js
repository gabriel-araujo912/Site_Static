function VEF8() /* <!-- Veloses e Furiosos 8 */ {
  const YouTube = document.querySelector('.VEF8');
  const Iframe = document.querySelector('iframe');
  YouTube.classList.toggle('active')
  Iframe.currentTime = 0;
  Iframe.pause();
}

